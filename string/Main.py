# LF (Line Feed)

print('Siapa namamu tuan?\nNamaku dizz')

# CR (Carriage Return)

print('Siapa namamu tuan?\rNamaku dizz')

# CRLF (Carriage Return Line Feed)

print('Siapa namamu tuan?\r\nNamaku dizz')

# string literal or raw

print(r'C:\New Folder')
print(r'\r \t \n')

# multiline literal string

print("""
Nama: Elgato\n
Ras: Kucing
""")

# multiline literal string dan raw

print(r"""
\t\n\r
""")
