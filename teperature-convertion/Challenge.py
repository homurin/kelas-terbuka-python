# fahrenheit to kelvin
fahrenheit = 1

celcius = ((5/9) * (fahrenheit - 32))
kelvin = celcius + 273

print(f"{fahrenheit} Fahrenheit to kelvin is {kelvin} Fahrenheit")

# kelvin to fahrenheit

kelvin = 1

celcius = kelvin - 273
fahrenheit = ((9/5) * celcius + 32)

print(f"{kelvin} Kelvin to Fahrenheit is {fahrenheit}")
