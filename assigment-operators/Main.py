# right shift (>>)

print("=====right shift======")
a = 0b00000010
print(f"value a is {a}, \t binary: {format(a, '08b')}")
a >>= 1
print(f"value a is {a}, \t binary: {format(a, '08b')}")

# left shift (>>)

print("=====left shift======")
a = 0b00000010
print(f"value a is {a}, \t binary: {format(a, '08b')}")
a <<= 1
print(f"value a is {a}, \t binary: {format(a, '08b')}")
